export function getTransactionPropertyWidth(
  subCategorySlug: string | undefined,
  propertyName: string,
): PropertyFieldWidth {
  if (!subCategorySlug) return "full"
  return propertyLayouts[subCategorySlug]?.[propertyName] || "full"
}
