export type PropertyFieldWidth = "full" | "half" | "third" | "twoThird"

type PropertyWidthMap = Partial<Record<string, PropertyFieldWidth>>

const createWidthMap = (
  full: string[] = [],
  half: string[] = [],
  third: string[] = [],
  twoThird: string[] = [],
): PropertyWidthMap => ({
  ...Object.fromEntries(full.map((field) => [field, "full"])),
  ...Object.fromEntries(half.map((field) => [field, "half"])),
  ...Object.fromEntries(third.map((field) => [field, "third"])),
  ...Object.fromEntries(twoThird.map((field) => [field, "twoThird"])),
})

